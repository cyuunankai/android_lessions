package com.example.slideimage;

public class ImageSrcIntent {

	int itemButtonSrc;
	int itemTextSrc;
	Class intentClass;
	
	public int getItemButtonSrc() {
		return itemButtonSrc;
	}
	public void setItemButtonSrc(int itemButtonSrc) {
		this.itemButtonSrc = itemButtonSrc;
	}
	public int getItemTextSrc() {
		return itemTextSrc;
	}
	public void setItemTextSrc(int itemTextSrc) {
		this.itemTextSrc = itemTextSrc;
	}
	public Class getIntentClass() {
		return intentClass;
	}
	public void setIntentClass(Class intentClass) {
		this.intentClass = intentClass;
	}
	
}
